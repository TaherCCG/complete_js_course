(function () {
  const header = document.querySelector('h1');
  header.style.color = 'red';

  document.querySelector('body').addEventListener('click', function () {
    header.style.color = 'blue';
  });
})();
// The IIFE is executed immediately, so the header variable is assigned to the h1 element. When the body is clicked, the event listener is triggered and the header's color is changed to blue. Since the header variable is in the same scope as the event listener, it can be accessed
